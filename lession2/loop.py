# ví dụ về for loop với list
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
  print(fruit)

# kết quả:
# apple
# banana
# cherry

# ví dụ về for loop với range
for i in range(1, 6):
  print(i)

# kết quả:
# 1
# 2
# 3
# 4
# 5



# ví dụ về while loop
i = 1
while i <= 5:
  print(i)
  i += 1

# kết quả:
# 1
# 2
# 3
# 4
# 5